#include <iostream>
#include <algorithm>
#include <cmath>
#include <bits/stdc++.h>

using namespace std;
typedef long long ll;

int main() {
    ll t;
    cin >> t;

    while (t --> 0){
        ll n;
        cin >> n;

        for (int i = 0; i < n; ++i) {
            cout << n << ' ';
        }
        cout << endl;
    }

    return 0;
}