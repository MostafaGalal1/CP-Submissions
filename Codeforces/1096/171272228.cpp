#include <iostream>
#include <algorithm>
#include <cmath>
#include <bits/stdc++.h>

using namespace std;
typedef long long ll;

int main(){
    ll t;
    cin >> t;

    while (t --> 0) {
        ll l, r;
        cin >> l >> r;

        cout << l << ' ' << l * 2 << endl;
    }
    return 0;
}